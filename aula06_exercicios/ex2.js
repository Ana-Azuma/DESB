// Cria um array vazio
let meuArray = [];

// Adiciona 3 números inteiros usando push()
meuArray.push(5);
meuArray.push(10);
meuArray.push(15);

// Exibe o array após adicionar os números
console.log("Array inicial:", meuArray);

// Substitui o primeiro elemento (índice 0) pelo dobro do valor atual
meuArray[0] = meuArray[0] * 2;

// Exibe o array atualizado
console.log("Array atualizado:", meuArray);
